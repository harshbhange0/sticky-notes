import { Provider } from "@/Provider";
import "./globals.css";
import { Inter } from "next/font/google";
import { Toaster } from "react-hot-toast";
import Sidebar from "./components/sidebar";
import CreateNots from "./components/CreateNots";
import { getServerSession } from "next-auth/next";
import { authOptions } from "@/lib/auth.config";
import RecoilContextProvider from "@/Provider/recoil";
import { Analytics } from "@vercel/analytics/react";
const inter = Inter({
  subsets: ["latin"],
  display: "swap",
  adjustFontFallback: false,
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const session = await getServerSession(authOptions);

  return (
    <html lang="en">
      <body className={inter.className}>
        <Toaster position="top-left" />
        <Provider>
          <RecoilContextProvider>
            <Sidebar>
              {session ? (
                <main className="w-full flex flex-col items-center h-full">
                  <CreateNots />
                  {children}
                  <Analytics
                    beforeSend={(e) => {
                      return e;
                    }}
                  />
                </main>
              ) : (
                <div className="h-[50vh] grid place-items-center w-full">
                  <p className=" w-full text-center">User Not Sign In!</p>
                </div>
              )}
            </Sidebar>
          </RecoilContextProvider>
        </Provider>
      </body>
    </html>
  );
}
